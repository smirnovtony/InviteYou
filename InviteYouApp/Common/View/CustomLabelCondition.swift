import UIKit

public func customLabelCondition(_ label: UILabel) {
    label.textColor = notСolorPink
    label.font = fontFamilyLittle?.withSize(18)
    label.textAlignment = .left
    label.translatesAutoresizingMaskIntoConstraints = false

}
